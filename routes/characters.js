const express = require('express');
const { getCharacters, getCharacter, createCharacter, updateCharacter, deleteCharacter } = require('../controllers/characters');
const router = express.Router();

router
    .route('/')
    .get(getCharacters)
    .post(createCharacter);

router
    .route('/:id')
    .get(getCharacter)
    .put(updateCharacter)
    .delete(deleteCharacter);

module.exports = router;
